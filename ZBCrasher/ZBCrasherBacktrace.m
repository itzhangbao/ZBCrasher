#import "ZBCrasherBacktrace.h"
#include <execinfo.h>

const NSInteger UncaughtExceptionHandlerSkipAddressCount = 4;
const NSInteger UncaughtExceptionHandlerReportAddressCount = 10;

@implementation ZBCrasherBacktrace

/**
 * Get function stack information
 */
+ (NSArray *)backtrace
{
    void* callstack[128];
    /**
     * It is used to obtain the function call stack of the current thread.
     * and returns the number of actually obtained pointers.
     */
    int frames = backtrace(callstack, 128);
    /**
     * The information obtained from the backtrace function is converted to an array of strings.
     */
    char **strs = backtrace_symbols(callstack, frames);
    int i;
    NSMutableArray *backtrace = [NSMutableArray arrayWithCapacity:frames];
    for (i = UncaughtExceptionHandlerSkipAddressCount;
         i < UncaughtExceptionHandlerSkipAddressCount+UncaughtExceptionHandlerReportAddressCount;
         i++)
    {
        [backtrace addObject:[NSString stringWithUTF8String:strs[i]]];
    }
    free(strs);
    return backtrace;
}

@end
